package org.example.algo.sorting.decreaseandconquer;

public class InsertionSort {
    public static void main(String[] args) {
        int[] numbers = {8, 5, 9, 6};

        for (int outerCounter = 1; outerCounter < numbers.length; outerCounter++) {
            int marker = 0;

            while(marker < outerCounter){

                marker++;
            }
        }
    }
}
